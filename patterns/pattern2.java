import java.util.*;
public class pattern2 {
    public static void main(String[]args){
        Scanner scn = new Scanner(System.in);
        int n = scn.nextInt();
        for(int i=n; i!=0; i--){
            for(int a= 1; a<=i; a++){
                System.out.print("*");
            }
            System.out.println();
            


        }
    }
    
}
